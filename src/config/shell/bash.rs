use indexmap::IndexMap;
use safe_relative_path::srpath;
use smol_str::SmolStr;

use crate::{
    config::Config,
    config_files::{ConfigFilePath, ConfigFileSource, ConfigFiles},
    error::Error,
};
use std::fmt::Write as _;

#[derive(serde::Deserialize, Debug, Clone, PartialEq)]
pub(in super::super) struct StoredBashConfig {
    environment: IndexMap<SmolStr, SmolStr>,
    alias: IndexMap<SmolStr, SmolStr>,
}

impl StoredBashConfig {
    pub fn make_config_files(
        &self,
        config: &Config,
        config_files: &mut ConfigFiles,
    ) -> Result<(), Error> {
        let Self { environment, alias } = self;
        let mut bash_config = String::new();

        _ = writeln!(
            bash_config,
            "#\n# Bash config generated by zenops, should be sourced from .bash_profile\n#\n"
        );

        #[cfg(target_os = "macos")]
        {
            _ = writeln!(bash_config, "# Homebrew environment (PATH, MANPATH, INFOPATH)");
            _ = writeln!(bash_config, "eval \"$(/opt/homebrew/bin/brew shellenv bash)\"");
            bash_config.push('\n');

            _ = writeln!(bash_config, "# Bash completions (brew-managed)");
            _ = writeln!(
                bash_config,
                "if [ -f $(brew --prefix)/etc/bash_completion ]; then"
            );
            _ = writeln!(bash_config, "    . $(brew --prefix)/etc/bash_completion");
            _ = writeln!(bash_config, "fi");
            bash_config.push('\n');
        }

        if config.has_cargo() {
            _ = writeln!(bash_config, "# Rust/Cargo environment");
            _ = writeln!(bash_config, ". \"$HOME/.cargo/env\"");
            bash_config.push('\n');
        }

        if !alias.is_empty() {
            for (name, value) in alias {
                _ = writeln!(bash_config, "alias {name}={value}");
            }
            bash_config.push('\n');
        }

        if !environment.is_empty() {
            for (name, value) in environment {
                _ = writeln!(bash_config, "export {name}={value}");
            }
            bash_config.push('\n');
        }

        _ = writeln!(bash_config, "[ -f ~/.bashrc ] && source ~/.bashrc");
        bash_config.push('\n');

        if config.has_sk() {
            _ = writeln!(bash_config, "# Setup sk (fuzzy find)");
            _ = writeln!(
                bash_config,
                "export SKIM_DEFAULT_COMMAND=\"fd --type f --hidden --exclude .git\""
            );
            _ = writeln!(bash_config, "source <(sk --shell bash)");
            _ = writeln!(bash_config, "source <(sk --shell bash --shell-bindings)");
            bash_config.push('\n');
        }

        if config.has_starship() {
            _ = writeln!(bash_config, "# Setup starship (fancy prompt)");
            _ = writeln!(bash_config, "eval \"$(starship init bash)\"");
            bash_config.push('\n');
        }

        if let Some(path) = config.path_variable() {
            _ = writeln!(bash_config, "export PATH={path}");
            bash_config.push('\n');
        }

        #[cfg(target_os = "macos")]
        if config.has_brew_llvm() {
            _ = writeln!(bash_config, "# LLVM compiler flags (brew-managed)");
            _ = writeln!(bash_config, "export LDFLAGS=-L/opt/homebrew/opt/llvm/lib");
            _ = writeln!(
                bash_config,
                "export CPPFLAGS=-L/opt/homebrew/opt/llvm/include"
            );
            bash_config.push('\n');
        }

        config_files.add(
            ConfigFilePath::in_home(srpath!(".zenops_bash_profile")),
            ConfigFileSource::Generated(bash_config),
        );

        Ok(())
    }
}
